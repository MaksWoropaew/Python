print("Введите день недели: ")
day_number = int(input())
if day_number == 1:
    print("Сегодня понедельник!")
elif day_number == 2:
    print("Сегодня вторник!")
elif day_number == 3:
    print("Сегодня среда!")
elif day_number == 4:
    print("Сегодня четверг!")
elif day_number == 5:
    print("Сегодня пятница!")  
elif day_number == 6:
    print("Сегодня суббота! Выходной")  
elif day_number == 7:
    print("Сегодня воскресенье! Выходной")
else: 
    print("Введите день недели от 1 до 7!!!")  

